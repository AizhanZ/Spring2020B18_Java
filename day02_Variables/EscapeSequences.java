package day02_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("\tHello");
        System.out.println("\t\t\t\tHello");
        System.out.println("\nHello");
        System.out.println("\nMy\nName");

        System.out.println("My favourite TV show is \"Game of Thrones\"");
            //

        System.out.println("\\");
            // prints single slash on console

        System.out.println("/");
        System.out.println("My favourite book is 'Java'"); // prints with single quote
        System.out.println("My favourite book is \'Java\'"); // also prints with single quote

    }

}
