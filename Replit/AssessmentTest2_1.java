package Replit;

public class AssessmentTest2_1 {
    /*
    Write a  for loop that prints the odd integers 11
    through 121 inclusive, separated by spaces.
     */

    public static void main(String[] args) {

        for(int num = 11; num <= 121; num += 2) {
            System.out.print(num + " ");
        }
    }

}
