package Replit;

public class Replit_081 {
    /*
    Write a program that will print out even numbers
    from 2 to 100 through the comma.
     */
    public static void main(String[] args) {

        for(int num = 2; num <= 100; num += 2 ){
            System.out.print(num + ",");
        }

    }
}
